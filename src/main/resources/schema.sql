DROP TABLE IF EXISTS SEARCH_KEYWORD_COUNT;
CREATE TABLE SEARCH_KEYWORD_COUNT (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    SEARCH_KEYWORD VARCHAR(255) NOT NULL,
    SEARCH_COUNT INT NOT NULL,
    VERSION BIGINT NOT NULL,
    PRIMARY KEY (ID)
);
CREATE UNIQUE INDEX "UQ_IDX_SEARCH_KEYWORD" ON "SEARCH_KEYWORD_COUNT" (SEARCH_KEYWORD);
CREATE INDEX "IDX_SEARCH_COUNT" ON "SEARCH_KEYWORD_COUNT" (SEARCH_COUNT);