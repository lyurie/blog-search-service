DROP TABLE IF EXISTS BLOG_SEARCH_KEYWORD_COUNT;
CREATE TABLE BLOG_SEARCH_KEYWORD_COUNT (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    SEARCH_KEYWORD VARCHAR(255) NOT NULL,
    SEARCH_COUNT INT NOT NULL,
    PRIMARY KEY (ID)
);
CREATE UNIQUE INDEX "UQ_IDX_BLOG_SEARCH_KEYWORD" ON "BLOG_SEARCH_KEYWORD_COUNT" (SEARCH_KEYWORD);